# 索引
name = ('nick', 'jenney')
a = name[0]
print(a)

# 获取指定元素的索引位置
name = ('nick', 'jenney')
a = name.index('nick')
print(a)

# 切片
name = ('nick', 'jenney')
a = name[0:1]
print(a)

# 计算元素出现的个数
name = ('nick', 'jenney')
a = name.count('nick')
print(a)

# 长度
name = ('nick', 'jenney')
a = len(name)
print(a)

# for循环
name = ('nick', 'jenney')
for i in name:
    print(i)